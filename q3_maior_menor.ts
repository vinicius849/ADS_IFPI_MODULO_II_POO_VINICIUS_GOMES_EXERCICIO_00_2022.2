/* Escreva um programa que leia três valores inteiros e apresente o
maior e o menor dos valores lidos. */

var num_1 : number = 2
var num_2 : number = 3
var num_3 : number = 4

// Raciocínio da questão será verificar Estruturas Condicionais
if (num_1 < num_2) {
    console.log('O número 1 é menor.')
}else if (num_2 < num_3) {
    console.log('O número 2 é menor.')
}else if (num_1 < num_3) {
    console.log('O número 1 é o menor entre os 3 números lidos.')
}

// Fim da questão
console.log('Método condicional aplicado.')
