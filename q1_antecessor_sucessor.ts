/* Escreva um programa que possua uma variável com um número ponto flutuante
e mostre seu antecessor e sucessor, ambos inteiros. */

let num_1: number = 8.5
let num_2: number = 8.6

// Raciocínio da questão
let antecessor = (num_1 - 1)
let sucessor = (num_2 + 1)

// Cálculo da questão
console.log('O número correspondente de seu antecessor será: ', antecessor)
console.log('O número correspondente de seu sucessor será: ', sucessor)
